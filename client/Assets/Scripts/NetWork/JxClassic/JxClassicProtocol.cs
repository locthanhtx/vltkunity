    public static class JxClassicProtocol
    {
        public const int GlobalProtocolType = 31;
        public const int NameLength = 64;

        public static string GetS2CName(byte protocol)
        {
            JxS2CProtocol value = (JxS2CProtocol)protocol;
            return Enum.IsDefined(typeof(JxS2CProtocol), value)
                ? value.ToString()
                : "s2c_unknown_" + protocol;
        }

        public static string GetC2SName(byte protocol)
        {
            JxC2SProtocol value = (JxC2SProtocol)protocol;
            return Enum.IsDefined(typeof(JxC2SProtocol), value)
                ? value.ToString()
                : "c2s_unknown_" + protocol;
        }
    }
